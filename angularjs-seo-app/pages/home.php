        <h1>AngularJS Crawling Test</h1>

        <p>This site is a single page AngularJS app. It's intended to test whether search engines can crawl and adequately render & index the angular pages</p>
		<ul>
		<li>No URLs are provided externally outside of this main / URL - no sitemaps, no links, no pinging</li>
		<li>Flat URLs - no hashbangs here</li>
		<li>No pre-rendering, or providing robots with the raw HTML of content - this is all Javascript, baby.</li>
		<li>Some links are provided in the core SPA HTML, others are on pages which must be rendered.</li>
		<li>All pages have titles, descriptions, canonical tags and robots directives - one page has "noindex".</li>
		</ul>
		<p>I've given all of the pages (relatively) unique titles to give robots something novel to read</p>
